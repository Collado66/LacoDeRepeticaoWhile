public class LacoDeRepeticaoWhile {
    public static void main(String[] args) {
        int i = 1; // Inicialização da variável

        // Laço de repetição 'while' para imprimir números de 1 a 100
        while (i <= 100) {
            System.out.println("Número: " + i);
            i++; // Incrementa o valor de 'i' a cada iteração
        }
    }
}
